#include <stdio.h>

int main() {

    // create the costPrice variable and assign 19 to it
    int costPrice = 19;

    // create the sellingPrice variable and assign 27 to it
    int sellingPrice = 27;

    // compute the profit and print it
    int profit = sellingPrice - costPrice;
    printf("Profit: %d\n", profit);

    return 0;
}
